#include <stdio.h>

int main() {

    // we use the '\n' escape sequence to add a new line 
    printf("      /|\n");
    printf("     / |\n");
    printf("    /  |\n");
    printf("   /   |\n");
    printf("  /    |\n");
    printf(" /     |\n");
    printf("/_ _ _ |\n");

    // this is a more practical example
    printf("This is the first sentence\n");
    printf("Second line\nThird Line\nFourth line\n\nAdded two spaces for thi sentence");

    return 0;
    
}